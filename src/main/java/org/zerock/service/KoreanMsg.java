package org.zerock.service;

public class KoreanMsg {

	
	private String hello;
	private String goodbye;
	
	public KoreanMsg(){
		this.hello = "안녕 세상아..";
		this.goodbye = "잘가 세상아..";
		
	}
	
	public String getHello() {
		return hello;
	}
	public String getGoodbye() {
		return goodbye;
	}
	
	
}
